// Fungsi untuk menampilkan modal dengan konten yang diberikan
function showModal(content) {
    // Membuat elemen modal
    var modal = document.createElement('div');
    modal.classList.add('modal');

    // Membuat elemen konten modal
    var modalContent = document.createElement('div');
    modalContent.classList.add('modal-content');

    // Membuat elemen untuk menampilkan konten
    var contentElement = document.createElement('p');
    contentElement.textContent = content;

    // Membuat tombol untuk menutup modal
    var closeButton = document.createElement('span');
    closeButton.classList.add('close');
    closeButton.innerHTML = '&times;';

    // Menambahkan event listener untuk menutup modal saat tombol ditutup
    closeButton.addEventListener('click', function() {
        modal.style.display = 'none';
    });

    // Memasukkan konten dan tombol tutup ke dalam modal
    modalContent.appendChild(contentElement);
    modalContent.appendChild(closeButton);
    modal.appendChild(modalContent);

    // Menambahkan modal ke dalam dokumen
    document.body.appendChild(modal);
}
